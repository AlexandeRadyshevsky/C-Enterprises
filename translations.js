function translatePage(lang) {
    const langData = translations[lang] || translations['ru'];
    
    document.documentElement.lang = lang;
    
    // Hero section
    const heroContent = document.getElementById('hero-content');
    if (heroContent) {
        heroContent.querySelector('h1').innerHTML = langData.hero_title;
        heroContent.querySelector('p').innerHTML = langData.hero_subtitle;
    }
    
    // About section
    const aboutSection = document.querySelector('#nas .text');
    if (aboutSection) {
        const aboutParagraphs = aboutSection.querySelectorAll('p');
        if (aboutParagraphs.length >= 2) {
            aboutParagraphs[0].innerHTML = langData.about_title;
            aboutParagraphs[1].innerHTML = langData.about_text;
        }
    }
    
    // Know section
    const knowSection = document.querySelector('#kols + .text');
    if (knowSection) {
        const knowParagraphs = knowSection.querySelectorAll('p');
        if (knowParagraphs.length >= 2) {
            knowParagraphs[0].innerHTML = langData.know_title;
            knowParagraphs[1].innerHTML = langData.know_text;
        }
    }
    
    // Choose section
    const chooseSections = document.querySelectorAll('.container.reverse');
    if (chooseSections.length >= 2) {
        const chooseSection = chooseSections[1];
        const chooseText = chooseSection.querySelector('.text');
        if (chooseText) {
            const chooseParagraphs = chooseText.querySelectorAll('p');
            if (chooseParagraphs.length >= 2) {
                chooseParagraphs[0].innerHTML = langData.choose_title;
                chooseParagraphs[1].innerHTML = langData.choose_text;
            }
        }
    }
    
    // For whom section
    const forWhomTitle = document.querySelector('.hero-text_in');
    if (forWhomTitle) forWhomTitle.textContent = langData.for_title;
    
    // Influencers
    const influencersTitle = document.querySelector('.left_gif h2');
    const influencersText = document.querySelector('.left_gif + .right_gif p');
    if (influencersTitle) influencersTitle.textContent = langData.influencers_title;
    if (influencersText) influencersText.innerHTML = langData.influencers_text;
    
    // Web3 projects
    const projectsTitle = document.querySelectorAll('.left_gif h2')[1];
    const projectsText = document.querySelectorAll('.left_gif + .right_gif p')[1];
    if (projectsTitle) projectsTitle.textContent = langData.projects_title;
    if (projectsText) projectsText.innerHTML = langData.projects_text;
    
    // Organizations
    const orgsTitle = document.querySelectorAll('.left_gif h2')[2];
    const orgsText = document.querySelectorAll('.left_gif + .right_gif p')[2];
    if (orgsTitle) orgsTitle.textContent = langData.orgs_title;
    if (orgsText) orgsText.innerHTML = langData.orgs_text;
    
    // Process letters
    const processLetters = document.querySelectorAll('.process_column span');
    if (processLetters.length === langData.process_title.length) {
        processLetters.forEach((span, i) => {
            span.textContent = langData.process_title[i];
        });
    }
    
    // Process steps
    const steps = document.querySelectorAll('[class*="number-block"]');
    if (steps.length >= 5) {
        steps[0].querySelector('p').textContent = langData.step1;
        steps[1].querySelector('p').textContent = langData.step2;
        steps[2].querySelector('p').textContent = langData.step3;
        steps[3].querySelector('p').textContent = langData.step4;
        steps[4].querySelector('p').textContent = langData.step5;
    }
    
    // Footer
    const footerTitle = document.querySelector('.footer-content h1');
    const footerText = document.querySelector('.footer-content .p');
    const contactBtn = document.querySelector('.footer-mid');
    
    if (footerTitle) footerTitle.textContent = langData.footer_title;
    if (footerText) footerText.innerHTML = langData.footer_text;
    if (contactBtn) contactBtn.textContent = langData.contact;
    
    // Navigation
    const navLinks = document.querySelectorAll('.nav a:not(.contact)');
    if (navLinks.length >= 3) {
        navLinks[0].textContent = langData.nav_about;
        navLinks[1].textContent = langData.nav_for;
        navLinks[2].textContent = langData.nav_process;
    }
    
    // Contact button
    const contactLink = document.querySelector('.nav a.contact');
    if (contactLink) contactLink.textContent = langData.nav_contact;
    
    localStorage.setItem('preferredLanguage', lang);
}

document.addEventListener('DOMContentLoaded', function() {
    const savedLang = localStorage.getItem('preferredLanguage') || 'ru';
    translatePage(savedLang);
    
    if (window.setLanguage) {
        window.setLanguage = function(lang) {
            translatePage(lang);
        };
    }
});